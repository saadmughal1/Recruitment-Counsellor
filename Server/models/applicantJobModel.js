const mongoose = require("mongoose");

const applicantJobSchema = new mongoose.Schema(
  {
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    title: {
      type: String,
      required: true,
      trim: true,
    },
    experienceRequired: {
      type: Number,
      default: 0,
      min: 0,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  { timestamps: true }
);

const ApplicantJob = mongoose.model("ApplicantJob", applicantJobSchema);

module.exports = ApplicantJob;
